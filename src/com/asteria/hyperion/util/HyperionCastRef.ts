/**
 * The list of default cast references for the Hyperion framework.
 */
export enum HyperionCastRef {

    /**
     * The reference to the <code>string</code> cast type.
     */
    STRING = 'string',
    
    /**
     * The reference to the <code>number</code> cast type.
     */
    NUMBER = 'number',
    
    /**
     * The reference to the <code>boolean</code> cast type.
     */
    BOOLEAN = 'boolean'
}
